<h4>Tabs Shortcode:</h4>
    <p>Displays blog posts in tab mode.</p>
    <pre><code>[ latest_post_tab ]</code></pre>
<h4>Shortcode with parameters:</h4>
    <pre><code>[ latest_post_tab number_of_posts= "3" category_id="6" ]</code></pre>
<h4>Shortcode settings:</h4>
    <pre>
<code>
"number_of_posts"       =&gt;   '3',    // Number of posts.<br>
"category_id"           =&gt;   '',     // Set an integer, category id='25'.<br>
</code></pre>

